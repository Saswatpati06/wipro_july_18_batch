package Java_Labs_21_07;

public class Arithematic 
{
      public static void main(String[] args) {
	        
	        int resA = -5 + 8 * 6;
	        int resB = (55 + 9) % 9;
	        int resC = 20 + -3 * 5 / 8;
	        int resD = 5 + 15 / 3 * 2 - 8 % 3;
	        
	        System.out.println("a. -5 + 8 * 6 = " + resA);
	        System.out.println("b. (55 + 9) % 9 = " + resB);
	        System.out.println("c. 20 + -3 * 5 / 8 = " + resC);
	        System.out.println("d. 5 + 15 / 3 * 2 - 8 % 3 = " + resD);
	    }
	}
