package Java_Labs_21_07;

public class Dowhilelab 
{
   public static void main(String[] args) {
        int i = 10; // Starting number

        // do-while loop to print numbers from 10 to 1
        do {
            System.out.println(i);
            i--; // Decrease the value of i
        } while (i >= 1);
    }
}
