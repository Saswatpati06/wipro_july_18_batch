package Java_Labs_21_07;

public class Datatype {
	    public static void main(String[] args) {
	        String text = "Hello";
	        int number = 42;
	        float decimal = 3.14f;
	        boolean isTrue = true;
	        char letter = 'A';
	        System.out.println("Value: " + text + " | Data Type: String");
	        System.out.println("Value: " + number + " | Data Type: int");
	        System.out.println("Value: " + decimal + " | Data Type: float");
	        System.out.println("Value: " + isTrue + " | Data Type: boolean");
	        System.out.println("Value: " + letter + " | Data Type: char");
	    }
	}


